package com.example.demo;

public class Dog {

	private String name;
	private String color;
	
	public Dog(String name, String color) {
		super();
		this.name = name;
		this.color = color;
	}
	
	public Dog() {
		
		
		
	}

	@Override
	public String toString() {
		return "Dog [name=" + name + ", color=" + color + "]";
	}
	
	
	
}
